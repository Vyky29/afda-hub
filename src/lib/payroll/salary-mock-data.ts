import type {
  SalaryStructureRecord,
  SalaryStructureSummary,
} from "./types";

function row(
  partial: Omit<
    SalaryStructureRecord,
    "grossTotal" | "totalDeductions" | "netSalary"
  > & {
    grossTotal?: number;
    totalDeductions?: number;
    netSalary?: number;
  }
): SalaryStructureRecord {
  const grossTotal =
    partial.grossTotal ??
    partial.baseSalary +
      partial.extraPay +
      partial.specificComplement +
      partial.otherComplements;
  const totalDeductions =
    partial.totalDeductions ??
    partial.socialSecurityDeduction +
      partial.irpfDeduction +
      partial.otherDeductions;
  const netSalary = partial.netSalary ?? grossTotal - totalDeductions;
  return {
    ...partial,
    grossTotal,
    totalDeductions,
    netSalary,
  };
}

