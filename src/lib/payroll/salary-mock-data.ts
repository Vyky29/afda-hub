export function computeSalarySummary(
  records: SalaryStructureRecord[]
): SalaryStructureSummary {
  return {
    totalGross: records.reduce((sum, r) => sum + r.grossTotal, 0),
    totalNet: records.reduce((sum, r) => sum + r.netSalary, 0),
    totalCompanyCost: records.reduce((sum, r) => sum + r.companyCost, 0),
    payslipsReviewed: records.filter((r) => r.reviewStatus === "reviewed")
      .length,
    payslipsPendingReview: records.filter(
      (r) => r.reviewStatus !== "reviewed"
    ).length,
  };
}
